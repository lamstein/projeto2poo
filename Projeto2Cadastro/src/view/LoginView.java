    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "E-mail e/ou senha invalidos");
            
        }else{
            try{
                System.out.println(jTextField2.getText()+" "+jTextField1.getText());
                Solicitacao solicitacao = new Solicitacao("LOG",jTextField2.getText(),jTextField1.getText());
                Conexao.startConnection();
                Client c = new Client(Conexao.getSocket());
                Conexao.getOutputStream().writeObject(solicitacao);
                Conexao.getOutputStream().flush();
                
                Solicitacao sol = (Solicitacao) Conexao.getInputStream().readObject();
                if(sol.getComando().equals("SUC")){
                    this.setVisible(false);
                    new MenuView().setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "E-mail e/ou Senha InvÃ¡lido(s)!", "Warning",JOptionPane.WARNING_MESSAGE);
                    Conexao.closeConnection();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    
                }
                
            }catch(Exception err){
                JOptionPane.showMessageDialog(null, err, "Warning",JOptionPane.WARNING_MESSAGE);
               
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed
    
