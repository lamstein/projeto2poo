    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(tf_nome.getText().equals("")||tf_email.getText().equals("")||tf_confirmarEmail.getText().equals("")||tf_senha.getText().equals("")||
                tf_confirmarSenha.getText().equals("")){
                     jLabel7.setText("");

              jLabel7.setText("Existem Campos em Branco!");
        }else{
            if(tf_nome.getText().equals("")){
                                jLabel7.setText("");

              jLabel7.setText("Campo Nome Inv√°lido!");
            }
            else if(tf_email.getText().equals(tf_confirmarEmail.getText())==false){
                jLabel7.setText("");
                jLabel7.setText("Campo E-mail e Confirmar E-mail n√£o conferem! ");
            }
            else if(tf_senha.getText().equals(tf_confirmarSenha.getText())==false){
                                jLabel7.setText("");

                jLabel7.setText("Campo Senha e Confirmar Senha n√£o conferem! ");
            }else{
                try{
                    Solicitacao solicitacao = new Solicitacao("CAD",tf_email.getText(),tf_nome.getText(),tf_senha.getText());
                    Conexao.startConnection();
                    
                    Conexao.getOutputStream().writeObject(solicitacao);
                    Conexao.getOutputStream().flush();

                    Solicitacao sol = (Solicitacao) Conexao.getInputStream().readObject();
                    System.out.println(sol.getComando().toString());
                    if(sol.getComando().equals("SUC")){
                      this.setVisible(false);
                                            Conexao.closeConnection();

                        JOptionPane.showMessageDialog(null, "Usu√°rio Cadastrado!", "Sucess",JOptionPane.WARNING_MESSAGE);
                      
                      new LoginView().setVisible(true);
                    }else{
                        this.setVisible(false);
                        JOptionPane.showMessageDialog(null, "Usu√°rio n√£o foi cadastrado!", "Warning",JOptionPane.WARNING_MESSAGE); 
                        Conexao.closeConnection();
                        new LoginView().setVisible(true);
                        

                    }
                }catch(Exception err){
                System.out.println(err);                    

                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

